CREATE TABLE members (
    id VARCHAR(64) PRIMARY KEY,
    tenant_id VARCHAR(64) NOT NULL REFERENCES tenants(id),
    branch_id VARCHAR(64) NOT NULL REFERENCES branches(id),
    membership_no VARCHAR(64) NOT NULL,
    full_name VARCHAR(160) NOT NULL,
    member_type VARCHAR(32) NOT NULL,
    phone VARCHAR(40) NOT NULL,
    email VARCHAR(160),
    national_id VARCHAR(80),
    password_hash VARCHAR(128) NOT NULL,
    password_salt VARCHAR(80) NOT NULL,
    status VARCHAR(32) NOT NULL,
    kyc_status VARCHAR(32) NOT NULL,
    joining_date DATE NOT NULL,
    savings_balance DECIMAL(18, 2) NOT NULL DEFAULT 0,
    shares_balance DECIMAL(18, 2) NOT NULL DEFAULT 0,
    welfare_balance DECIMAL(18, 2) NOT NULL DEFAULT 0,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UNIQUE (tenant_id, membership_no)
);

CREATE INDEX idx_members_tenant_status ON members (tenant_id, status);
CREATE INDEX idx_members_tenant_branch ON members (tenant_id, branch_id);

